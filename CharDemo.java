package practice;
// program to work with Ascii and unicodes in java
public class CharDemo {

	public static void main(String[] args) {
		
		char ch ='d';
		int value=ch;
		System.out.println("The Ascii value of character "+ch+" = "+value);
		
		
		char ch1='\u03C0';
		System.out.println("The symbol of PI = "+ch1);		

	}

}
